import Foundation

// Default measurement is in inches
struct Foot {
  var footCirc: Double = 9.0 // Circumference of foot at widest point
  var footLength: Double = 9.0 // Length of bottom of foot from big toe to heel
  var calfCirc: Double = 9.0 // Circumference of calf at widest point at top of sock
}
